package Entidades;

/**
 *
 * @author joans
 */
public class Comarca {
    
    private int id;
    private String nombre;

    public Comarca() {
    }

    public Comarca(String nombre) {
        this.nombre = nombre;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }
    
    
    
}
